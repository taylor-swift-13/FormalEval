Require Import List.
Require Import ZArith.
Require Import Arith.
Require Import Nat.
Require Import Lia.
Require Import Coq.Arith.PeanoNat.
Open Scope Z_scope.
Import ListNotations.

Definition problem_5_pre (input output : list Z) : Prop := True.

Definition problem_5_spec (input output : list Z) (d : Z) : Prop :=
  (input = [] -> output = []) /\
  (forall n : nat,
      length input = n -> (1 <= n)%nat ->
      length output = (2 * n - 1)%nat /\
      forall i : nat, (i < 2 * n - 1)%nat ->
        (Nat.Even i -> nth_error output i = nth_error input (i / 2)) /\
        (Nat.Odd i -> nth_error output i = Some d)
  ).

Fixpoint intersperse (l : list Z) (d : Z) : list Z :=
  match l with
  | [] => []
  | [x] => [x]
  | x :: xs => x :: d :: intersperse xs d
  end.

Example test_case : problem_5_spec [] (intersperse [] 7%Z) 7%Z.
Proof.
  unfold problem_5_spec.
  split.
  - intros H. simpl. reflexivity.
  - intros n Hlen Hn. simpl in Hlen. lia.
Qed.
Import ListNotations.
Open Scope Z_scope.

Definition factorial (n : Z) : Z :=
  let fix inner (m : nat) : Z :=
    match m with
    | O => 1
    | S m' => Z.of_nat m * inner m'
    end
  in inner (Z.to_nat n).

Definition sum_to_n (n : Z) : Z :=
  let fix inner (m : nat) : Z :=
    match m with
    | O => 0
    | S m' => Z.of_nat m + inner m'
    end
  in inner (Z.to_nat n).

Definition f_spec (n : Z) (result : list Z) : Prop :=
  result = map (fun i => if Z.even i then factorial i else sum_to_n i) (map Z.of_nat (seq 1 (Z.to_nat n))).


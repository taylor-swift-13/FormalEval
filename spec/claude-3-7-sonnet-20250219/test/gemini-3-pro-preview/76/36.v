Require Import ZArith.
Require Import Lia.
Open Scope Z_scope.

Definition is_simple_power_spec (x n : Z) (b : bool) : Prop :=
  b = true <->
    (x = 1) \/ 
    (n = 0 /\ x = 0) \/ 
    (n = 1 /\ x = 1) \/ 
    (n = -1 /\ (x = 1 \/ x = -1)) \/
    exists k : Z,
      (0 <= k) /\
      (Z.abs (Z.pow n k) <= Z.abs x) /\
      (Z.pow n k = x).

Example test_case : is_simple_power_spec 81 81 true.
Proof.
  unfold is_simple_power_spec.
  split.
  - intros _.
    right. right. right. right.
    exists 1.
    split.
    + lia.
    + split.
      * simpl. lia.
      * simpl. reflexivity.
  - intros _.
    reflexivity.
Qed.
Require Import Coq.Lists.List.
Require Import Coq.ZArith.ZArith.
Require Import Coq.Bool.Bool.
Import ListNotations.
Open Scope Z_scope.

Definition count_occurrences (lst : list Z) (x : Z) : Z :=
  fold_right (fun n c => if Z.eq_dec n x then c + 1 else c) 0 lst.

Definition search (lst : list Z) : Z :=
  let valid (x : Z) := andb (Z.gtb x 0) (Z.leb x (count_occurrences lst x)) in
  let candidates := filter valid lst in
  fold_right Z.max (-1) candidates.

Example test_search:

Require Import Coq.Lists.List.
Require Import Coq.ZArith.ZArith.
Import ListNotations.
Open Scope Z_scope.

Definition count_occurrences (l : list Z) (x : Z) : Z :=
  fold_right (fun y c => if Z.eq_dec y x then c + 1 else c) 0 l.

Definition solve (l : list Z) : Z :=
  let filtered := filter (fun x => if Z.eq_dec (count_occurrences l x) x then true else false) l in
  fold_right Z.max (-1) filtered.

Example test_case:
